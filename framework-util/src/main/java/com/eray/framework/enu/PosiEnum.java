public enum PosiEnum {
	
	B("前")
	, A("后")
	, N("无")
	;
     
    private String name;
    
    private PosiEnum(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}